public final class PreDefinedShortCut {

	public int quickId;
	public int imageRes;
	public String name;

	public PreDefinedShortCut() { }

	public PreDefinedShortCut(int quickId, int imageRes, String name) {
		this.quickId = quickId;
		this.imageRes = imageRes;
		this.name = name;
	}

}
